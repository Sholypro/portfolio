  <p id="presentation" class="Puno" >
    Notre agence est un groupe de fan du jeux vidéos et d’E-Sport qui en
    2020 lors d’un tournoi organisé entre amis décide de se professionnaliser
    et de créer Uniplay. Notre but est simple, nous souhaitons partager l’ambiance et l’amusement que l’on retrouve lors de nos parties endiablées
    entre amis tout en y mêlant un aspect compétitif car nous apprécions
    aussi l’E-Sport. C’est d’ici que nous est venu l’idée de Unichamps, un
    tournoi mêlant convivialité et compétitivité.
    Cela fait donc plusieurs mois que nous travaillons sur le projet Unichamps afin d’offrir à nos joueurs la meilleure expérience de jeu possible
    ainsi qu’un spectacle grandiose pour nos spectateurs.
  </p>

  <p id="presentation" class="Pdos">
    Qu’est-ce qu’Unichamps ? Il s’agit tout bonnement de notre premier tournoi officiel !
    L’évènement se déroulera durant le week-end 26 février 2022 et nous encourageons vivement les membres de l’IUT de Bobigny a participer, les joueurs s’affronteront sur plusieurs jeux différent et gratuit affins que tout le monde ai ses chances de gagner. Notre objectif est d’avoir minimum 8 équipes de 5 joueurs et seul les 3 meilleurs recevront un lot de cadeaux a partagé entre amis. L’entièreté du tournoi sera diffusé sur le site de l’évènement mais également sur les plateformes de streaming live notamment sur notre compte Twitch et Youtube, alors nous trépidons d’impatience de vous y retrouver pour la compétition !
  </p>



